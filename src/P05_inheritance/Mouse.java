public class Mouse {

	public void leftClick() {
		System.out.println("Lclick!");
	}
	
	public void rightClick() {
		System.out.println("Rclick!");
	}

	public void scrollUp() {
		System.out.println("Scrolled Up!");
	}
	
	public void scrolldown() {
		System.out.println("Scrolled Down!");
	}
}
